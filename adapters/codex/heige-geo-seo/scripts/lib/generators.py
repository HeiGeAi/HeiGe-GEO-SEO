def gen_article(title, description="", author=None, org=None, url=None,
                date_published=None, date_modified=None, image=None,
                author_url=None, same_as=None):
    node = {
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": title,
    }
    if description:
        node["description"] = description
    if image:
        node["image"] = image
    if date_published:
        node["datePublished"] = date_published
    if date_modified or date_published:
        node["dateModified"] = date_modified or date_published
    if url:
        node["mainEntityOfPage"] = {"@type": "WebPage", "@id": url}
    if author:
        a = {"@type": "Person", "name": author}
        if author_url:
            a["@id"] = author_url
        if same_as:
            a["sameAs"] = same_as
        node["author"] = a
    if org:
        node["publisher"] = {"@type": "Organization", "name": org}
    return node


def gen_faqpage(qa_pairs):
    """qa_pairs: list of (question, answer)."""
    return {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": [
            {"@type": "Question", "name": q,
             "acceptedAnswer": {"@type": "Answer", "text": a}}
            for q, a in qa_pairs
        ],
    }


def gen_howto(name, steps):
    """steps: list of (step_name, step_text)."""
    return {
        "@context": "https://schema.org",
        "@type": "HowTo",
        "name": name,
        "step": [
            {"@type": "HowToStep", "position": i + 1, "name": sn, "text": st}
            for i, (sn, st) in enumerate(steps)
        ],
    }


def gen_product(name, description="", brand=None, price=None, currency="CNY",
                rating=None, rating_count=None):
    node = {
        "@context": "https://schema.org",
        "@type": "Product",
        "name": name,
    }
    if description:
        node["description"] = description
    if brand:
        node["brand"] = {"@type": "Brand", "name": brand}
    if price is not None:
        node["offers"] = {"@type": "Offer", "price": str(price),
                          "priceCurrency": currency}
    if rating is not None:
        agg = {"@type": "AggregateRating", "ratingValue": str(rating)}
        if rating_count is not None:
            agg["ratingCount"] = str(rating_count)
        node["aggregateRating"] = agg
    return node


def gen_organization(name, url=None, logo=None, same_as=None, founder=None,
                     description=""):
    """Entity-layer schema. @id + sameAs are the AI-citation gatekeepers."""
    node = {
        "@context": "https://schema.org",
        "@type": "Organization",
        "name": name,
    }
    if url:
        node["@id"] = url.rstrip("/") + "/#org"
        node["url"] = url
    if description:
        node["description"] = description
    if logo:
        node["logo"] = {"@type": "ImageObject", "url": logo}
    if same_as:
        node["sameAs"] = same_as
    if founder:
        node["founder"] = {"@type": "Person", "name": founder}
    return node


def gen_person(name, job_title=None, url=None, knows_about=None, same_as=None,
               works_for=None):
    node = {
        "@context": "https://schema.org",
        "@type": "Person",
        "name": name,
    }
    if url:
        node["@id"] = url
    if job_title:
        node["jobTitle"] = job_title
    if knows_about:
        node["knowsAbout"] = knows_about
    if same_as:
        node["sameAs"] = same_as
    if works_for:
        node["worksFor"] = {"@type": "Organization", "name": works_for}
    return node


def gen_website(name, url, publisher=None):
    node = {
        "@context": "https://schema.org",
        "@type": "WebSite",
        "name": name,
        "url": url,
    }
    if url:
        node["@id"] = url.rstrip("/") + "/#website"
    if publisher:
        node["publisher"] = {"@type": "Organization", "name": publisher}
    return node


def gen_breadcrumb(items):
    """BreadcrumbList。知识库 04 标为全站每页建议上。
    items: list of (name, url) 或 (name,)(最后一项可无 url)。"""
    elements = []
    for i, item in enumerate(items, 1):
        name = item[0] if isinstance(item, (list, tuple)) else item
        el = {"@type": "ListItem", "position": i, "name": name}
        if isinstance(item, (list, tuple)) and len(item) > 1 and item[1]:
            el["item"] = item[1]
        elements.append(el)
    return {"@context": "https://schema.org", "@type": "BreadcrumbList",
            "itemListElement": elements}


def gen_itemlist(name, items):
    """ItemList。知识库 04/08 标为'最好的 X'/榜单对比页命中尤其有效。
    items: list of (name, url) 或 dict(可嵌 Product)。"""
    elements = []
    for i, item in enumerate(items, 1):
        if isinstance(item, dict):
            el = {"@type": "ListItem", "position": i, "item": item}
        else:
            nm = item[0] if isinstance(item, (list, tuple)) else item
            el = {"@type": "ListItem", "position": i, "name": nm}
            if isinstance(item, (list, tuple)) and len(item) > 1 and item[1]:
                el["url"] = item[1]
        elements.append(el)
    return {"@context": "https://schema.org", "@type": "ItemList",
            "name": name, "itemListElement": elements}


def gen_review(item_name, rating, author, body="", best_rating="5"):
    """Review。知识库 04 产品/工具页建议上。"""
    node = {
        "@context": "https://schema.org",
        "@type": "Review",
        "itemReviewed": {"@type": "Thing", "name": item_name},
        "reviewRating": {"@type": "Rating", "ratingValue": str(rating),
                         "bestRating": str(best_rating)},
        "author": {"@type": "Person", "name": author},
    }
    if body:
        node["reviewBody"] = body
    return node


def gen_graph(*nodes):
    """把多个 schema 节点用 @graph 串成一个图(知识库 04 第2节核心原则:用 @id 关联,
    别各自孤立)。每个节点去掉自带 @context,顶层统一一个。"""
    graph = []
    for n in nodes:
        if not n:
            continue
        m = dict(n)
        m.pop("@context", None)
        graph.append(m)
    return {"@context": "https://schema.org", "@graph": graph}


def to_script(node):
    body = json.dumps(node, ensure_ascii=False, indent=2)
    return '<script type="application/ld+json">\n%s\n</script>' % body


