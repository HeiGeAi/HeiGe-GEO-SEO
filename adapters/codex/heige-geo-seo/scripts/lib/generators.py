def gen_llms_txt(site_name, summary, sections=None, body=None):
    """sections: list of {'title': str, 'links': [{'name','url','desc'}]}"""
    out = ["# %s" % site_name, ""]
    if summary:
        out.append("> %s" % summary)
        out.append("")
    if body:
        out.append(body.strip())
        out.append("")
    for sec in (sections or []):
        out.append("## %s" % sec["title"])
        for link in sec.get("links", []):
            desc = link.get("desc")
            line = "- [%s](%s)" % (link["name"], link["url"])
            if desc:
                line += ": %s" % desc
            out.append(line)
        out.append("")
    return "\n".join(out).rstrip() + "\n"


def parse_links_file(text):
    """Parse a simple links file into one section.
    Each line: url | name | description   (description optional)."""
    links = []
    for raw in text.splitlines():
        line = raw.strip()
        if not line or line.startswith("#"):
            continue
        parts = [p.strip() for p in line.split("|")]
        url = parts[0]
        name = parts[1] if len(parts) > 1 and parts[1] else url
        desc = parts[2] if len(parts) > 2 else None
        links.append({"name": name, "url": url, "desc": desc})
    return links


