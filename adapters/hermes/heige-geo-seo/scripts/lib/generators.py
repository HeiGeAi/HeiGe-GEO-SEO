_SEARCH_BOTS = ["Googlebot", "Bingbot", "Baiduspider"]
_AI_SEARCH_BOTS = ["OAI-SearchBot", "ChatGPT-User", "Claude-SearchBot",
                   "Claude-User", "PerplexityBot", "Perplexity-User"]
_AI_TRAIN_BOTS = ["GPTBot", "ClaudeBot", "Google-Extended", "CCBot",
                  "Applebot-Extended"]


def gen_robots(strategy="allow-all", sitemap=None, disallow_paths=None):
    """strategy:
      allow-all    -- maximize AI visibility, allow everything
      expose-only  -- allow AI search/citation, block training crawlers
      cn-index     -- China-first: ensure Baidu/Sogou/Bing index access
    """
    disallow_paths = disallow_paths or ["/admin/", "/api/"]
    lines = ["# Generated by HeiGe-GEO-SEO", "# strategy: %s" % strategy, ""]

    def block(ua_list, rule):
        for ua in ua_list:
            lines.append("User-agent: %s" % ua)
            lines.append(rule)
            lines.append("")

    if strategy == "allow-all":
        block(_SEARCH_BOTS + _AI_SEARCH_BOTS + _AI_TRAIN_BOTS, "Allow: /")
    elif strategy == "expose-only":
        lines.append("# 搜索引擎与 AI 检索/引用类:放行")
        lines.append("")
        block(_SEARCH_BOTS + _AI_SEARCH_BOTS, "Allow: /")
        lines.append("# AI 训练类:拒绝(只要曝光不喂训练)")
        lines.append("")
        block(_AI_TRAIN_BOTS, "Disallow: /")
    elif strategy == "cn-index":
        lines.append("# 国内 GEO:确保百度/搜狗/必应能收录(国产 AI 的总进水口)")
        lines.append("")
        block(["Baiduspider", "Sogou web spider", "Bingbot", "Googlebot",
               "YisouSpider", "360Spider", "HaoSpider"], "Allow: /")
        lines.append("# 神马(YisouSpider,UC/夸克移动)、360(360Spider)一并放行")
        lines.append("")
        lines.append("# AI 检索类一并放行")
        lines.append("")
        block(_AI_SEARCH_BOTS, "Allow: /")
        lines.append("# 字节 Bytespider 抓取激进,如需限制在防火墙层加 IP/UA 拦截")
        lines.append("")
    else:
        raise ValueError("unknown strategy: %s" % strategy)

    lines.append("# 兜底")
    lines.append("User-agent: *")
    lines.append("Allow: /")
    for p in disallow_paths:
        lines.append("Disallow: %s" % p)
    lines.append("")
    if sitemap:
        lines.append("Sitemap: %s" % sitemap)
    return "\n".join(lines).rstrip() + "\n"


