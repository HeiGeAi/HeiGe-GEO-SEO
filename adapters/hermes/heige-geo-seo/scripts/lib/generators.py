_AI_BOTS_3LAYER = [
    ("GPTBot", "train"), ("OAI-SearchBot", "search"), ("ChatGPT-User", "user"),
    ("ClaudeBot", "train"), ("Claude-SearchBot", "search"), ("Claude-User", "user"),
    ("PerplexityBot", "search"), ("Google-Extended", "train"),
]


def gen_ai_txt(allow_train=False, citation_required=True):
    """ai.txt — content usage policy, separate from robots.txt."""
    lines = ["# ai.txt — content usage policy",
             "# Generated by HeiGe-GEO-SEO", ""]
    for bot, kind in _AI_BOTS_3LAYER:
        if kind == "train" and not allow_train:
            lines.append("User-Agent: %s" % bot)
            lines.append("Disallow: /")
            lines.append("Content-Usage: train=no")
        else:
            lines.append("User-Agent: %s" % bot)
            lines.append("Allow: /")
            if kind == "search" and citation_required:
                lines.append("Citation: required")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def gen_robots_patch(allow_train=True):
    """robots.patch — surgical diff that only adds AI-bot allow lines."""
    adds = ["GPTBot", "OAI-SearchBot", "ClaudeBot", "Claude-SearchBot",
            "PerplexityBot", "Google-Extended"] if allow_train else \
           ["OAI-SearchBot", "Claude-SearchBot", "PerplexityBot"]
    lines = ["--- a/robots.txt", "+++ b/robots.txt", "@@ AI crawlers @@"]
    for ua in adds:
        lines.append("+User-agent: %s" % ua)
        lines.append("+Allow: /")
    return "\n".join(lines) + "\n"


def gen_sitemap(urls):
    """urls: list of (loc, lastmod) or just loc strings."""
    out = ['<?xml version="1.0" encoding="UTF-8"?>',
           '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">']
    for u in urls:
        if isinstance(u, (list, tuple)):
            loc, lastmod = u[0], (u[1] if len(u) > 1 else None)
        else:
            loc, lastmod = u, None
        if lastmod:
            out.append("  <url><loc>%s</loc><lastmod>%s</lastmod></url>" % (loc, lastmod))
        else:
            out.append("  <url><loc>%s</loc></url>" % loc)
    out.append("</urlset>")
    return "\n".join(out) + "\n"


def gen_answers_json(qa_items):
    """answers.json — top Q&A in machine-citable format.
    qa_items: list of (q, a, url) or (q, a)."""
    answers = []
    for item in qa_items:
        q, a = item[0], item[1]
        rec = {"q": q, "a": a}
        if len(item) > 2 and item[2]:
            rec["url"] = item[2]
        answers.append(rec)
    return json.dumps({"version": "1.0", "answers": answers},
                      ensure_ascii=False, indent=2) + "\n"


def gen_citations_json(citations):
    """citations.json — first-party sources backing site claims.
    citations: list of dict(claim, source, date[, id])."""
    out = []
    for i, c in enumerate(citations, 1):
        rec = {"id": c.get("id", i), "claim": c["claim"], "source": c["source"]}
        if c.get("date"):
            rec["date"] = c["date"]
        out.append(rec)
    return json.dumps({"citations": out}, ensure_ascii=False, indent=2) + "\n"


def gen_humans_txt(team=None, site=None, standards=None, last_update=None):
    """humans.txt — author / team metadata feeding E-E-A-T author signal."""
    lines = ["/* TEAM */"]
    for role, name in (team or []):
        lines.append("%s: %s" % (role, name))
    lines.append("")
    lines.append("/* SITE */")
    if site:
        lines.append("Site: %s" % site)
    if standards:
        lines.append("Standards: %s" % ", ".join(standards))
    if last_update:
        lines.append("Last update: %s" % last_update)
    return "\n".join(lines) + "\n"


def gen_feed_xml(title, link, items, last_build=None):
    """feed.xml — RSS freshness signal. items: list of (title, pubdate[, link])."""
    out = ['<?xml version="1.0" encoding="UTF-8"?>',
           '<rss version="2.0"><channel>',
           "  <title>%s</title>" % title,
           "  <link>%s</link>" % link]
    if last_build:
        out.append("  <lastBuildDate>%s</lastBuildDate>" % last_build)
    for item in items:
        t, pub = item[0], item[1]
        il = item[2] if len(item) > 2 else link
        out.append("  <item><title>%s</title><link>%s</link><pubDate>%s</pubDate></item>"
                   % (t, il, pub))
    out.append("</channel></rss>")
    return "\n".join(out) + "\n"


